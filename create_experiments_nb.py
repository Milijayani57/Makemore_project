    "ax.plot(bs2, [r['val']   for r in batch_results], 's--', label='val')\n"
    "ax.set_xlabel('Batch size')\n"
    "ax.set_title('Exp 2 — Batch Size')\n"
    "ax.legend(); ax.grid(True)\n"
    "\n"
    "# Architecture\n"
    "ax = axes[2]\n"
    "labels = [r['label'] for r in arch_results]\n"
    "x = range(len(labels))\n"
    "ax.bar([i-0.2 for i in x], [r['train'] for r in arch_results], 0.4, label='train')\n"
    "ax.bar([i+0.2 for i in x], [r['val']   for r in arch_results], 0.4, label='val', alpha=0.7)\n"
    "ax.set_xticks(list(x)); ax.set_xticklabels(labels)\n"
    "ax.set_ylabel('NLL Loss'); ax.set_title('Exp 3 — Architecture Size')\n"
    "ax.legend(); ax.grid(True, axis='y')\n"
    "\n"
    "plt.suptitle('Hyperparameter Experiments — makemore (50 000 steps each)', fontsize=13)\n"
    "plt.tight_layout()\n"
