    "plt.show()\n"
    "\n"
    "# Log summary image to MLflow (arbitrary run — open a throwaway run)\n"
    "with mlflow.start_run(run_name='hyperparameter-summary', tags={'experiment': 'summary'}):\n"
    "    mlflow.log_artifact(img_path)\n"
    "\n"
    "# Print numeric tables\n"
    "print('\\n--- Context Length ---')\n"
    "print(f'{\"block_size\":>12} {\"train\":>10} {\"val\":>10}')\n"
    "for r in ctx_results:\n"
    "    print(f'{r[\"block_size\"]:>12} {r[\"train\"]:>10.4f} {r[\"val\"]:>10.4f}')\n"
    "\n"
    "print('\\n--- Batch Size ---')\n"
    "print(f'{\"batch_size\":>12} {\"train\":>10} {\"val\":>10}')\n"
    "for r in batch_results:\n"
    "    print(f'{r[\"batch_size\"]:>12} {r[\"train\"]:>10.4f} {r[\"val\"]:>10.4f}')\n"
